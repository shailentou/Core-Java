package com.shail.common.Sorting;

public class Employee {

	private String eName;
	private int eid;
	public Employee(String eName, int eid) {
		super();
		this.eName = eName;
		this.eid = eid;
	}
	public String geteName() {
		return eName;
	}
	public void seteName(String eName) {
		this.eName = eName;
	}
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	
	
}
