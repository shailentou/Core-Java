package com.shail.common.Sorting;

public class NotComparable {

		public int i;
		public NotComparable(int i) {
			this.i=i;
		}
		public int getI() {
			return i;
		}
		public void setI(int i) {
			this.i = i;
		}
		@Override
		public String toString() {
			return "NotComparable [i=" + i + "]";
		}
		
		
}
